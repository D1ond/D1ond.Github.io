$(document).ready(function(){
  $(window).scroll(function () {
   if ($(this).scrollTop() > 10) { 
        $('#topgo').fadeIn();
   } else {
       $('#topgo').fadeOut();
   }
  });

  $('#topgo').click(function () {
   $('body,html').animate({
        scrollTop:0
   }, 500);
        return false;
  });      
});


