        $('#bottomgo').fadeIn();
   }
   else{
       $('#bottomgo').fadeOut();
   }
  });

  $('#bottomgo').click(function () {
   $('body,html').animate({
        scrollTop:$(document).height() -$(window).height()
   }, 500);
        return false;
  });      
});




    

